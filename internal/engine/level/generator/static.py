    @staticmethod
    def drawSpaceship(game, platforms):
        """Desenhar nave espacial na última plataforma ao invés da bandeira"""
        last_platform = platforms[-1]  # Última plataforma da lista
        
        # Posicionar a nave acima da última plataforma
